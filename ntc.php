<h2><font color="#FFFFFF">¿QUE ES LA LICENCIATURA EN NUTRICION ?</h2>
<h3><font color="#FFFFFF">El licenciado en Nutrición es un profesional capaz de brindar atención nutriológica a individuos sanos, en riesgo o enfermos, así como a grupos de los diferentes sectores de la sociedad; administrar servicios y programas de alimentación y nutrición; proponer, innovar y mejorar la calidad nutricional y sanitaria de productos alimenticios.

Es capaz de integrar, generar y aplicar conocimientos, habilidades y actitudes que permiten su desempeño en los campos profesionales básicos: nutrición clínica, nutrición poblacional, servicios de alimentos, ciencia de los alimentos y otros campos transversales, como investigación, educación, administración y consultoría, y aplica métodos, técnicas y tecnologías propias de la nutriología y ciencias afines.

Fundamenta su ejercicio profesional en un marco ético y multidisciplinario para responder con calidad y compromiso a las necesidades sociales de alimentación y nutrición presentes y futuras</h3>

